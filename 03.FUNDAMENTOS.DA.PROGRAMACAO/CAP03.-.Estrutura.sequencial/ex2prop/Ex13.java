package CAP03.ex2prop;

import java.util.Scanner;

public class Ex13 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.printf("Digite um numero ");
        int numb = sc.nextInt();
        System.out.println("0 * "+numb+" = "+(0*numb));
        System.out.println("1 * "+numb+" = "+(1*numb));
        System.out.println("2 * "+numb+" = "+(2*numb));
        System.out.println("3 * "+numb+" = "+(3*numb));
        System.out.println("4 * "+numb+" = "+(4*numb));
        System.out.println("5 * "+numb+" = "+(5*numb));
    }
}
